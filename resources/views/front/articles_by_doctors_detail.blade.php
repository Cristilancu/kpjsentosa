                                    <h4 class="blog-title">Kesedaran Penting Hindar Kanser Serviks</h4>
                                    <p class="blog-meta">By: <a href="#">Dr Andi Anggeriana Andi Asri</a> | Specialty: <a href="specialty_gynae_oncology.html">Gynae-Oncology</a>, <a href="specialty_orthopaedic_surgery.html">Obstetric &amp; Gynaecology</a></p>
                                    
                                    <div class="blog-thumbnail">
                                        <img src="images/articles_by_doctors/kesedaran_penting_hindar_kanser_serviks/img_dr_andi_large.jpg" alt="Kesedaran Penting Hindar Kanser Serviks">
                                      <div class="blog-date"><p class="day">13</p><p class="monthyear">MAR, 2016</p></div>
                                    </div>
                                    
                                  <div class="blog-content">
    
                                    	<p>Jangan pandang remeh jika didatangi darah haid lebih banyak dan berpanjangan daripada biasa. Dan jika telah menopaus tetapi tiba-tiba terdapat pendarahan seperti haid serta sakit melampau di bahagian serviks, berjumpalah doktor atau jalani ujian pap smear setahun sekali.</p>
                                        <p>Ini kerana kanser serviks boleh menyerang wanita pada usia seawal 20 tahun. Bagaimanapun menurut Pakar Ginekologi Onkologi Hospital KPJ Sentosa Kuala Lumpur, Dr. Andi Anggeriana Andi Asri pesakit paling muda yang mendapat rawatan kanser serviks ialah berusia 24 tahun. </p>
                                        <p>"Kanser serviks adalah kanser nombor dua atau tiga selepas kanser payudara atau kanser kolon. </p>
                                    	<p>"Kajian menunjukkan kanser serviks berada di tempat kedua atau ketiga berganti kedudukan dengan kanser kolon bagi wanita.</p>
                                        <p>"Ini berdasarkan kajian yang dilakukan oleh Pertubuhan Kesihatan Sedunia (WHO) pada tahun 2012," katanya semasa ditemui di pejabatnya baru-baru ini. </p>
                                        <p>Menurut Dr. Andi Anggeriana, kanser pangkal rahim boleh dikesan seawal tahap satu. </p>
                                        <p>Biasanya 75 peratus penyakit disebabkan oleh jangkitan Human Papillomavirus (HPV) dan dikawal dengan suntikan vaksin yang diberikan pada pelajar sekolah berusia seawal 13 tahun diikuti 10 tahun kemudian. </p>
                                        <p>Pangkal rahim atau serviks adalah bahagian pangkal atau bawah rahim yang bersambung dengan vagina. Kanser pangkal rahim berlaku apabila sel-sel di serviks membiak secara luar biasa.</p>
                                        <p>Kanser serviks terbahagi kepada dua iaitu peringkat awal yang berlaku di sekitar serviks dan peringkat lewat apabila sel kanser mula merebak ke tisu-tisu berhampiran atau organ tubuh yang lain seperti paru-paru melalui sistem aliran darah. </p>
                                        <p>"Kanser serviks merupakan kanser kedua terbanyak dalam kalangan wanita selepas kanser payudara. Di Malaysia, senario yang sama berlaku dan terdapat peningkatan kes sejak kebelakangan ini," katanya.</p>
                                        <p>Kajian WHO menganggarkan lebih kurang 18 orang wanita daripada setiap 100,000 penduduk menghidap kanser serviks di mana 7.9 peratus (2,000 hingga 3,000 kes setiap tahun) daripada semua kes kanser yang dimasukkan ke hospital kerajaan adalah terdiri dari kanser serviks. Manakala 10.5 peratus daripada kematian wanita yang menghidap kanser di hospital kerajaan disebabkan kanser serviks.</p>
                                        <p>WHO melaporkan bahawa terdapat lebih daripada 700 wanita di Malaysia maut akibat kanser serviks setiap tahun. Punca sebenarnya juga masih menjadi misteri namun daripada kajian saintis, terjadinya prakanser dan kanser serviks adalah disebabkan oleh jangkitan (HPV).</p>
                                        <p>Terdapat lebih daripada 100 jenis HPV dan kajian menunjukkan 99.7 peratus daripada kes kanser serviks mempunyai kaitan secara langsung dengan virus HPV jenis 16 dan 18, sementara jenis 6 dan 11 pula dikaitkan dengan ketuat kelamin.</p>
                                        <p>Dr. Andi Anggeriana berkata, secara umumnya, jangkitan HPV berlaku tanpa disedari kerana tidak menunjukkan sebarang simptom atau gejala klinikal dan akan sembuh dalam tempoh sembilan hingga 12 bulan. Walau bagaimanapun dianggarkan 20 peratus daripada mereka yang dijangkiti akan mendapat jangkitan kronik HPV yang berlarutan melebihi 18 bulan. </p>
                                        <p>"Atas sebab-sebab inilah maka ujian pap smear perlu dilakukan bagi mengesan sel luar biasa sebelum sel kanser serviks mula membentuk dan dikesan. Realitinya pada masa dan saat ini hanya ujian pap smear mampu mengesan sel luar biasa dan keputusan rawatan bergantung kepada jenis sel kanser yang berjaya dikesan.</p>
                                    <p>"Selain itu ubatan yang digunakan pada tahun 1946/47 untuk menghentikan pendarahan semasa hamil meningkatkan risiko kanser pangkal rahim pada usia muda," katanya. </p>
                                                           
